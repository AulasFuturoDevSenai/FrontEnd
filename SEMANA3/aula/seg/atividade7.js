let numero = parseInt(prompt("Digite um número para descobrir a raiz"));

// Calcula e exibe os resultados no console
console.log("Raiz quadrada: " + Math.sqrt(numero));
console.log("Quadrado do número: " + Math.pow(numero, 2));
console.log("Dividido por 3 (com 3 casas decimais): " + (numero/3).toFixed(3));